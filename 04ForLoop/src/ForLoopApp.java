
public class ForLoopApp {

	public static void main(String[] args) {
		System.out.println("For Loops/n");
		
		//count to ten
		for (int i = 0; i<=10; i++) {
			System.out.println("i is "+i);
			
		}
		
		for (int i = 100; i>0; i--) {
			System.out.println(i);
		}
		
		for (int i = 0; i<100; i+=10) {
			System.out.println("+"+i);
		}
		
		
		
		
		
		System.out.println("\nBye");
	}

}
